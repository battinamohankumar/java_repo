package First_Package;

public class Variables {

	public static void main(String[] args) {
		
		String name = "Mohan"; // String - stores text(String values are surrounded by "double quotes")
		System.out.println(name);

		int I_num = 25;
		System.out.println(I_num); // int - stores integers( without decimals, such as 123 or -123)

		float D_num = 3.26f;
		System.out.println(D_num); // float-stores floating point numbers, with decimals(19.99 or -19.99)

		char C_name = 'M';
		System.out.println(C_name); // char-stores single characters,'a' or 'B'Char values are surrounded by 'single quotes')

		boolean myBool = true;
		System.out.println(myBool); //boolean - stores values with two states( true or false)

	}

}
